package com.happiness.servinghands;

public class Toy {
    public String age, type, UID, Username, Email, Phone;

    public Toy() {
    }

    public Toy(String age, String type, String UID, String username, String email, String phone) {
        this.age = age;
        this.type = type;
        this.UID = UID;
        Username = username;
        Email = email;
        Phone = phone;
    }

}
