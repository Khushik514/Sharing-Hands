package com.happiness.servinghands;

public class Cloth {
    public String age, gender, season, UID, Username, Email, Phone;

    public Cloth() {
    }

    public Cloth(String age, String gender, String season, String UID, String username, String email, String phone) {
        this.age = age;
        this.gender = gender;
        this.season = season;
        this.UID = UID;
        Username = username;
        Email = email;
        Phone = phone;
    }
}
