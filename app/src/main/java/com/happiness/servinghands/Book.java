package com.happiness.servinghands;

public class Book {
    public String BookName, AuthorName, BookCategory, BookAges;

    public Book() {
    }

    public Book(String bookName, String authorName, String bookCategory, String bookAges) {
        BookName = bookName;
        AuthorName = authorName;
        BookCategory = bookCategory;
        BookAges = bookAges;
    }
}
